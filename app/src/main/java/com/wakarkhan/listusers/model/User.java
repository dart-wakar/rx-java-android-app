public class User {

    private String first_name;
    private String username;
    private String email;
    private String phone;

    public String getFirst_name() {
        return first_name;
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }

    public String getPhone() {
        return phone;
    }

}
